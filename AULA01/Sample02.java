package AULA01;

public class Sample02 {
    public static void main(String[] args) {
        
        //Operadores aritméticos: + - / * % ( % é resto da divisão )
        
        //abreviação: syso - preenche a linha abaixo automaticamente

        //atalho para executar (control + f5)
        System.out.println(2 + 3 * 4);
          
        System.out.println((2 + 3) * 4);

        System.out.println( 15 % 2);

        // + com texto faz a concatenação
        System.out.println("A soma de 2+3 =" + (2+3));

        // \n quebra linha
        System.out.println("Este \ntexto deve \nsair em várias\n linhas.");
    }
}